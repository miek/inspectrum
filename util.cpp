#include <algorithm>
#include "util.h"

template <class T> const T& clamp (const T& value, const T& min, const T& max)
{
    return std::min(max, std::max(min, value));
}
template const float& clamp(const float&, const float&, const float&);
template const int& clamp(const int&, const int&, const int&);
